//Operações aritméticas
var v1 = 12.5;
var v2 = 4.7;
console.log("---------------------------------------");
console.log("Valores: v1 =", v1, "| v2 =", v2);
console.log("---------------------------------------");
console.log("Soma: v1+v2 =", v1 + v2);
console.log("subtração: v1-v2 =", v1 - v2);
console.log("multiplicação: v1*v2 =", v1 * v2);
console.log("Divisão: v1/v2 =", v1 / v2);

v1 = 10;
